// Function to get current language from URL parameters
function getCurrentLanguage() {
    const urlParams = new URLSearchParams(window.location.search);
    const lang = urlParams.get('lang');
    return lang && translations[lang] ? lang : 'en';
}

// Function to get current currency from URL parameters
function getCurrentCurrency() {
    const urlParams = new URLSearchParams(window.location.search);
    const currency = urlParams.get('currency');
    return currency && currencyConfig[currency] ? currency : 'USD';
}

// Function to format currency amount
function formatCurrency(amount, currency = null) {
    const activeCurrency = currency || currentCurrency;
    const config = currencyConfig[activeCurrency];
    
    if (!config) {
        return `$ ${parseFloat(amount).toFixed(2)}`;
    }
    
    // Format amount with appropriate decimal places
    const formattedAmount = parseFloat(amount).toFixed(config.decimals);
    
    // Return symbol + space + amount
    return `${config.symbol} ${formattedAmount}`;
}

// Function to get currency symbol only
function getCurrencySymbol(currency = null) {
    const activeCurrency = currency || currentCurrency;
    const config = currencyConfig[activeCurrency];
    return config ? config.symbol : '$';
}

// Function to get decimal places for currency
function getCurrencyDecimals(currency = null) {
    const activeCurrency = currency || currentCurrency;
    const config = currencyConfig[activeCurrency];
    return config ? config.decimals : 2;
}

// Function to get translated text
function t(key, replacements = {}) {
    let translation;
    
    // Try current language first
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
        translation = translations[currentLanguage][key];
    }
    // Fallback to English
    else if (translations['en'] && translations['en'][key]) {
        translation = translations['en'][key];
    }
    // Last resort: return the key itself
    else {
        translation = key;
    }
    
    // Handle replacements like {amount} and {currency}
    let result = translation;
    for (const [placeholder, value] of Object.entries(replacements)) {
        result = result.replace(new RegExp(`{${placeholder}}`, 'g'), value);
    }
    
    return result;
}

// Function to get translated Biden phrases
function getBidenPhrases() {
    return translations[currentLanguage] && translations[currentLanguage].bidenPhrases
        ? translations[currentLanguage].bidenPhrases
        : translations['en'].bidenPhrases;
}

// Initialize language on page load
function initializeLanguage() {
    currentLanguage = getCurrentLanguage();
    currentCurrency = getCurrentCurrency();
    
    
    // Ensure we have a valid language
    if (!translations[currentLanguage]) {
        currentLanguage = 'en';
    }
}

// Update UI elements with translations
function updateUITranslations() {
    
    // Update all elements with data-translate attribute
    const translatableElements = document.querySelectorAll('[data-translate]');
    
    translatableElements.forEach(element => {
        const key = element.getAttribute('data-translate');
        
        if (key) {
            const translation = t(key);
            element.textContent = translation;
        }
    });
    
    // Update toolbar labels specifically
    const balanceLabels = document.querySelectorAll('.toolbar-label');
    balanceLabels.forEach(label => {
        if (label.getAttribute('data-translate') === 'balance') {
            label.textContent = t('balance');
        } else if (label.getAttribute('data-translate') === 'amount') {
            label.textContent = t('amount');
        }
    });
    
    // Update turbo button title based on current state
    const turboButton = document.getElementById('turbo-btn');
    if (turboButton) {
        const isActive = turboButton.classList.contains('active') || turboButton.title.includes('ON');
        turboButton.title = isActive ? t('turboModeOn') : t('turboModeOff');
    }
    
    // Update volume button title
    const volumeButton = document.getElementById('volume-btn');
    if (volumeButton) {
        volumeButton.title = t('volume');
    }
    
    // Update game message if it's showing the default message
    const gameMessage = document.getElementById('game-message');
    if (gameMessage && gameMessage.textContent === 'Place your bet to start!') {
        gameMessage.textContent = t('comeAndPlay');
    }
    
}

